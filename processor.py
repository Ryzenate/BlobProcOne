OEM_PATTERNS = {
    "ASUS":    [r"\basus\b", r"\basustek\b"],
    "HP":      [r"\bhp\b", r"\bhewlett.?packard\b", r"\bompaq\b"],
    "Lenovo":  [r"\blenovo\b", r"\bthinkpad\b", r"\bideapad\b"],
    "Dell":    [r"\bdell\b", r"\blatitude\b", r"\binspiron\b", r"\bxps\b"],
    "AMD":     [r"\bamd\b", r"\bradeon\b", r"\bryzen\b"],
    "Generic": [],
}

COMPONENT_PATTERNS = {
    "BIOS":    [r"\bbios\b", r"\buefi\b", r"\bfirmware\b", r"\bflash\b"],
    "GFX":     [r"\bgfx\b", r"\bgraphics\b", r"\bgpu\b", r"\bradeon\b", r"\bdisplay\b", r"\bvideo\b"],
    "Chipset": [r"\bchipset\b", r"\bamd.?b\d{3}\b", r"\bamd.?x\d{3}\b"],
    "NPU":     [r"\bnpu\b", r"\bneural\b", r"\bai\b", r"\bxdna\b"],
    "WLAN":    [r"\bwlan\b", r"\bwifi\b", r"\bwireless\b", r"\bwi.fi\b", r"\b802\.11\b"],
}

